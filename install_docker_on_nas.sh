#!/bin/bash

# Цвета для вывода
BLUE='\033[0;34m'
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[0;33m'
NC='\033[0m' # No Color

# Информация для пользователя
cat << EOF
${BLUE}========================================================${NC}
${GREEN}Установка Docker на Synology NAS${NC}
${BLUE}========================================================${NC}

К сожалению, Docker не может быть установлен автоматически через SSH.
Вам необходимо установить Docker через веб-интерфейс Synology DSM:

1. Откройте веб-браузер и введите адрес вашего NAS: http://192.168.1.124
2. Войдите в систему, используя свои учетные данные DSM
3. Откройте центр пакетов (Package Center) в DSM
4. В поле поиска введите "Docker"
5. Нажмите кнопку "Установить" для пакета Docker
6. Дождитесь завершения установки

После установки Docker:

1. Убедитесь, что Docker запустился
2. Вернитесь к терминалу и запустите скрипт:
   ${YELLOW}./deploy_to_nas.sh${NC}

${BLUE}========================================================${NC}
EOF

# Проверка соединения с NAS
echo -e "${BLUE}Проверка соединения с Synology NAS (192.168.1.124)...${NC}"
if ping -c 1 192.168.1.124 &> /dev/null; then
    echo -e "${GREEN}Соединение с NAS установлено${NC}"
    echo -e "${YELLOW}Перейдите по адресу http://192.168.1.124 для установки Docker через веб-интерфейс${NC}"
else
    echo -e "${RED}Не удалось подключиться к NAS по адресу 192.168.1.124${NC}"
    echo -e "${YELLOW}Проверьте подключение и IP-адрес NAS${NC}"
fi 